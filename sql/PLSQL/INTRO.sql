DECLARE
   A NUMBER := NULL;
   B NUMBER := 2;
   C NUMBER;

BEGIN

  IF  A IS NULL OR B IS NULL THEN
    C := 3;
  ELSIF A < B THEN
    C := 1;
  ELSE
    C := 2;
  END IF;
  DBMS_OUTPUT.PUT_LINE ('El valor de C es ' || C);

END;

DECLARE 
NOTA NUMBER(2);
BEGIN 
	NOTA :=8;

	IF NOTA >= 9 THEN
		DBMS_OUTPUT.PUT_LINE('SOBRESALIENTE');
	ELSIF NOTA >= 7 THEN
		DBMS_OUTPUT.PUT_LINE('NOTABLE');
	ELSIF NOTA=6 THEN 
		DBMS_OUTPUT.PUT_LINE('BIEN');
	ELSIF NOTA=5 THEN
		DBMS_OUTPUT.PUT_LINE('SUFICIENTE');
	ELSIF NOTA<5 AND NOTA >=0 THEN 
		DBMS_OUTPUT.PUT_LINE('INSUFUCIENTE');
	ELSE 
		DBMS_OUTPUT.PUT_LINE('NOTA NO VALIDA');
	END IF;
END;


